-- Provenance FK on articles; rows keep their denormalized source_* columns
-- (the client needs them in one row) — this adds integrity + joinability.
alter table public.articles
  add column if not exists source_id uuid references public.sources(id) on delete set null;

update public.articles a
   set source_id = s.id
  from public.sources s
 where a.feed_url = s.url
   and a.source_id is null;
