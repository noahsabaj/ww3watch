-- Feeds become DATA: the roster was operational state trapped in code
-- (feeds die, get WAF-blocked, need curating — all of that is now columns).
-- The pipeline loads 'where enabled' and writes health back every run; the
-- About page renders the roster; curation becomes UPDATEs instead of commits.

create table if not exists public.sources (
  id uuid primary key default gen_random_uuid(),
  url text unique not null,
  name text not null,
  region text not null,
  lang text not null,
  enabled boolean not null default true,
  last_ok_at timestamptz,
  last_via text,
  consecutive_failures int not null default 0,
  last_error_kind text,
  last_error text,
  updated_at timestamptz not null default now()
);

